package com.techelevator.model;

public class Host {
    private int host_id;
    private String host_name;
    private String host_description;
    private String host_img;

    public int getHost_id() {
        return host_id;
    }

    public void setHost_id(int host_id) {
        this.host_id = host_id;
    }

    public String getHost_name() {
        return host_name;
    }

    public void setHost_name(String host_name) {
        this.host_name = host_name;
    }

    public String getHost_description() {
        return host_description;
    }

    public void setHost_description(String host_description) {
        this.host_description = host_description;
    }

    public String getHost_img() {
        return host_img;
    }

    public void setHost_img(String host_img) {
        this.host_img = host_img;
    }
}


