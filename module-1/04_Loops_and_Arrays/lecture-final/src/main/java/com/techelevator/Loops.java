package com.techelevator;

public class Loops {


    public static void main(String[] args) {

        //be able to build a for loop that loops from 1 to 100

        for (int i=0; i<10; i++) {
            System.out.println("done");
        }


        // i++ will increment after the statement is run
        // ++i will increment first THEN execute the statement
        int x = 1;
        System.out.println(++x);





    }



}
